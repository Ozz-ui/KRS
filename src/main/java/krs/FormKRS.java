    private void loadData() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID KRS");
    model.addColumn("NIM");
    model.addColumn("Nama Mahasiswa");
    model.addColumn("Semester");
    model.addColumn("Tahun Ajaran");
    model.addColumn("Status");

    database db = new database();
    try (Connection con = db.koneksi();
         Statement st = con.createStatement();
         ResultSet rs = st.executeQuery(
             "SELECT id_krs, nim, nama_mahasiswa, semester, " +
             "tahun_ajaran, status FROM v_krs_lengkap " +
             "GROUP BY id_krs, nim, nama_mahasiswa, semester, tahun_ajaran, status " +
             "ORDER BY id_krs DESC")) {

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id_krs"),
                rs.getString("nim"),
                rs.getString("nama_mahasiswa"),
                rs.getInt("semester"),
                rs.getString("tahun_ajaran"),
                rs.getString("status")
            });
        }

    } catch (SQLException e) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Error: " + e.getMessage(), "Error",
            javax.swing.JOptionPane.ERROR_MESSAGE);
    }

    jTable1.setModel(model);
}

