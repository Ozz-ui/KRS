    private void SetujuiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SetujuiActionPerformed
        int baris = jTable1.getSelectedRow();
        if (baris == -1) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Pilih KRS terlebih dahulu!",
                "Peringatan", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        int idKrs = (int) jTable1.getValueAt(baris, 0);
        String status = (String) jTable1.getValueAt(baris, 5);

        if (status.equals("Disetujui")) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "KRS ini sudah disetujui!", "Info",
                javax.swing.JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        database db = new database();
        try (Connection con = db.koneksi();
             PreparedStatement ps = con.prepareStatement(
                 "UPDATE krs SET status='Disetujui' WHERE id_krs=?")) {

            ps.setInt(1, idKrs);
            ps.executeUpdate();
            javax.swing.JOptionPane.showMessageDialog(this,
                "KRS berhasil disetujui!", "Sukses",
                javax.swing.JOptionPane.INFORMATION_MESSAGE);
            loadData();

        } catch (SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Error: " + e.getMessage(), "Error",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_SetujuiActionPerformed

    private void TolakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TolakActionPerformed
        int baris = jTable1.getSelectedRow();
        if (baris == -1) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Pilih KRS terlebih dahulu!",
                "Peringatan", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        int idKrs = (int) jTable1.getValueAt(baris, 0);
        String status = (String) jTable1.getValueAt(baris, 5);

        if (status.equals("Ditolak")) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "KRS ini sudah ditolak!", "Info",
                javax.swing.JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        String catatan = javax.swing.JOptionPane.showInputDialog(this,
            "Masukkan alasan penolakan (opsional):",
            "Tolak KRS", javax.swing.JOptionPane.QUESTION_MESSAGE);

        if (catatan == null) return;

        database db = new database();
        try (Connection con = db.koneksi();
             PreparedStatement ps = con.prepareStatement(
                 "UPDATE krs SET status='Ditolak', catatan=? WHERE id_krs=?")) {

            ps.setString(1, catatan.isEmpty() ? null : catatan);
            ps.setInt(2, idKrs);
            ps.executeUpdate();
            javax.swing.JOptionPane.showMessageDialog(this,
                "KRS berhasil ditolak!", "Sukses",
                javax.swing.JOptionPane.INFORMATION_MESSAGE);
            loadData();

        } catch (SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Error: " + e.getMessage(), "Error",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_TolakActionPerformed

    private void HapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HapusActionPerformed
        int baris = jTable1.getSelectedRow();
        if (baris == -1) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Pilih KRS terlebih dahulu!",
                "Peringatan", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        int konfirmasi = javax.swing.JOptionPane.showConfirmDialog(this,
            "Yakin ingin menghapus KRS ini?",
            "Konfirmasi Hapus", javax.swing.JOptionPane.YES_NO_OPTION);

        if (konfirmasi != javax.swing.JOptionPane.YES_OPTION) return;

        int idKrs = (int) jTable1.getValueAt(baris, 0);

        database db = new database();
        try (Connection con = db.koneksi();
             PreparedStatement ps = con.prepareStatement(
                 "DELETE FROM krs WHERE id_krs=?")) {

            ps.setInt(1, idKrs);
            ps.executeUpdate();
            javax.swing.JOptionPane.showMessageDialog(this,
                "KRS berhasil dihapus!", "Sukses",
                javax.swing.JOptionPane.INFORMATION_MESSAGE);
            loadData();

        } catch (SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Error: " + e.getMessage(), "Error",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_HapusActionPerformed

