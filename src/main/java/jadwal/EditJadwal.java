    private void loadKelas() {
        comboKelas.removeAllItems();
        database db = new database();
        try (Connection con = db.koneksi();
             Statement st = con.createStatement();
             ResultSet rs = st.executeQuery(
                 "SELECT k.id_kelas, mk.nama_mk FROM kelas k " +
                 "JOIN mata_kuliah mk ON k.kode_mk = mk.kode_mk " +
                 "ORDER BY k.id_kelas")) {

            while (rs.next()) {
                comboKelas.addItem(rs.getInt("id_kelas") + " - " + rs.getString("nama_mk"));
            }

        } catch (SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Error: " + e.getMessage(), "Error",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }

    private void loadHari() {
        comboHari.removeAllItems();
        comboHari.addItem("Senin");
        comboHari.addItem("Selasa");
        comboHari.addItem("Rabu");
        comboHari.addItem("Kamis");
        comboHari.addItem("Jumat");
        comboHari.addItem("Sabtu");
    }
    
    private void setComboKelas(int idKelas) {
        for (int i = 0; i < comboKelas.getItemCount(); i++) {
            if (comboKelas.getItemAt(i).startsWith(idKelas + " - ")) {
                comboKelas.setSelectedIndex(i);
                break;
            }
        }
    }

    private void setComboHari(String hari) {
        for (int i = 0; i < comboHari.getItemCount(); i++) {
            if (comboHari.getItemAt(i).equals(hari)) {
                comboHari.setSelectedIndex(i);
                break;
            }
        }
    }
