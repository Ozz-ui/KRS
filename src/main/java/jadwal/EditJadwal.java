    private void TambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TambahActionPerformed
        String selectedKelas = comboKelas.getSelectedItem().toString();
        int idKelas  = Integer.parseInt(selectedKelas.split(" - ")[0]);
        String hari  = comboHari.getSelectedItem().toString();
        String jamMul = fieldMulai.getText().trim();
        String jamSel = fieldSelesai.getText().trim();
        String ruang  = fieldRuang.getText().trim();

        if (jamMul.isEmpty() || jamSel.isEmpty() || ruang.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Semua field harus diisi!",
                "Peringatan", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (!jamMul.matches("\\d{2}:\\d{2}") || !jamSel.matches("\\d{2}:\\d{2}")) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Format jam harus HH:mm, contoh: 08:00",
                "Peringatan", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        database db = new database();
        try (Connection con = db.koneksi();
             PreparedStatement ps = con.prepareStatement(
                 "UPDATE jadwal SET id_kelas=?, hari=?, jam_mulai=?, jam_selesai=?, ruang=? " +
                 "WHERE id_jadwal=?")) {

            ps.setInt(1, idKelas);
            ps.setString(2, hari);
            ps.setString(3, jamMul);
            ps.setString(4, jamSel);
            ps.setString(5, ruang);
            ps.setInt(6, idJadwal);
            ps.executeUpdate();

            javax.swing.JOptionPane.showMessageDialog(this,
                "Jadwal berhasil diupdate!", "Sukses",
                javax.swing.JOptionPane.INFORMATION_MESSAGE);

            new FormJadwal().setVisible(true);
            this.dispose();

        } catch (SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Error: " + e.getMessage(), "Error",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_TambahActionPerformed

