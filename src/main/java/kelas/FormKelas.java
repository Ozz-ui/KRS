    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
    java.sql.Connection c = null;
    try {
    c = getConn();
    c.setAutoCommit(false);
    java.sql.PreparedStatement psK = c.prepareStatement(
        "INSERT INTO kelas (kode_mk, id_dosen, tahun_ajaran, semester) VALUES (?,?,?,?)",
        java.sql.Statement.RETURN_GENERATED_KEYS);
    String kodeMK =
    jComboBox1.getSelectedItem()
    .toString()
    .split(" - ")[0];

    int idDosen =
    Integer.parseInt(
        jComboBox2.getSelectedItem()
        .toString()
        .split(" - ")[0]
    );

    psK.setString(1, kodeMK);
    psK.setInt(2, idDosen);
    psK.setString(3, txtTahunAjaran.getText());
    psK.setString(4, txtSemester.getText());
    psK.executeUpdate();
    java.sql.ResultSet gk = psK.getGeneratedKeys();
    int idBaru = 0;
    if (gk.next()) idBaru = gk.getInt(1);
    java.sql.PreparedStatement psJ = c.prepareStatement(
        "INSERT INTO jadwal (id_kelas, hari, jam_mulai, jam_selesai, ruang) VALUES (?,?,?,?,?)");
    psJ.setInt(1, idBaru);
    psJ.setString(2, cbHari.getSelectedItem().toString());
    psJ.setString(3, txtJamMulai.getText());
    psJ.setString(4, txtJamSelesai.getText());
    psJ.setString(5, txtRuang.getText());
    psJ.executeUpdate();
    c.commit();
    javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!");
    
    
    bersihForm();
    loadData();
    c.close();
} catch (java.sql.SQLException ex) {
    try { if (c != null) c.rollback(); } catch (java.sql.SQLException ignored) {}
    javax.swing.JOptionPane.showMessageDialog(this, ex.getMessage());
}        // TODO add your handling code here:
    }//GEN-LAST:event_btnTambahActionPerformed

