    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
    if (txtIdKelas.getText().isEmpty()) {
    javax.swing.JOptionPane.showMessageDialog(this, "Pilih data di tabel dulu!");
    return;
}
int pilih = javax.swing.JOptionPane.showConfirmDialog(this, "Yakin hapus data ini?");
if (pilih == javax.swing.JOptionPane.YES_OPTION) {
    java.sql.Connection c = null;
    try {
        c = getConn();
        c.setAutoCommit(false);
        int idK = Integer.parseInt(txtIdKelas.getText());
        java.sql.PreparedStatement psJ = c.prepareStatement("DELETE FROM jadwal WHERE id_kelas=?");
        psJ.setInt(1, idK);
        psJ.executeUpdate();
        java.sql.PreparedStatement psK = c.prepareStatement("DELETE FROM kelas WHERE id_kelas=?");
        psK.setInt(1, idK);
        psK.executeUpdate();
        c.commit();
        javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
        bersihForm();
        loadData();
        c.close();
    } catch (java.sql.SQLException ex) {
        try { if (c != null) c.rollback(); } catch (java.sql.SQLException ignored) {}
        javax.swing.JOptionPane.showMessageDialog(this, ex.getMessage());
    }
}        // TODO add your handling code here:
    }//GEN-LAST:event_btnHapusActionPerformed

