    private void loadData() {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblKelas.getModel();
        model.setRowCount(0);
        model.setColumnIdentifiers(new String[]{"ID Kelas","Kode MK","ID Dosen","Thn Ajaran","Semester","Hari","Jam Mulai","Jam Selesai","Ruang"});
        String sql = "SELECT k.id_kelas, k.kode_mk, k.id_dosen, k.tahun_ajaran, k.semester, "
                   + "j.hari, j.jam_mulai, j.jam_selesai, j.ruang "
                   + "FROM kelas k LEFT JOIN jadwal j ON k.id_kelas = j.id_kelas";
        try {
            java.sql.Connection c = getConn();
            java.sql.Statement s = c.createStatement();
            java.sql.ResultSet r = s.executeQuery(sql);
            while (r.next()) {
                model.addRow(new Object[]{
                    r.getInt("id_kelas"),
                    r.getString("kode_mk"),
                    r.getInt("id_dosen"),
                    r.getString("tahun_ajaran"),
                    r.getString("semester"),
                    r.getString("hari"),
                    r.getString("jam_mulai"),
                    r.getString("jam_selesai"),
                    r.getString("ruang")
                });
            }
            c.close();
        } catch (java.sql.SQLException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }

    private java.sql.Connection getConn() throws java.sql.SQLException {
        return java.sql.DriverManager.getConnection("jdbc:mysql://localhost:3306/krs", "root", "");
    }

