    private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
        // TODO add your handling code here:
        try {

        if (TotalSksDipilih.getText().equals("0")) {
            JOptionPane.showMessageDialog(this,
                    "Pilih mata kuliah terlebih dahulu!");
            return;
        }

        String sqlKrs =
                "INSERT INTO krs " +
                "(nim, semester, tahun_ajaran, status) " +
                "VALUES (?, ?, ?, ?)";

        PreparedStatement ps =
                conn.prepareStatement(
                        sqlKrs,
                        PreparedStatement.RETURN_GENERATED_KEYS
                );

        ps.setString(1, Nim.getText());
        ps.setInt(2,
                Integer.parseInt(
                        Semester.getText()));

        ps.setString(3,
                TahunAjaran.getText());

        ps.setString(4,
                "Menunggu");

        ps.executeUpdate();

        ResultSet rs = ps.getGeneratedKeys();

        int idKrs = 0;

        if (rs.next()) {
            idKrs = rs.getInt(1);
        }

        // simpan detail krs
        for (int i = 0;
                i < jTable1.getRowCount();
                i++) {

            Boolean pilih =
                    (Boolean) jTable1
                            .getValueAt(i, 0);

            if (pilih != null && pilih) {

                int idKelas =
                        Integer.parseInt(
                                jTable1
                                        .getValueAt(i, 1)
                                        .toString());

                String sqlDetail =
                        "INSERT INTO krs_detail " +
                        "(id_krs, id_kelas) " +
                        "VALUES (?, ?)";

                PreparedStatement ps2 =
                        conn.prepareStatement(
                                sqlDetail);

                ps2.setInt(1, idKrs);
                ps2.setInt(2, idKelas);

                ps2.executeUpdate();
            }
        }

        JOptionPane.showMessageDialog(this,
                "KRS berhasil disimpan!");

    } catch (Exception e) {

        JOptionPane.showMessageDialog(this,
                "Gagal simpan : "
                + e.getMessage());
    }
    }//GEN-LAST:event_SimpanActionPerformed

