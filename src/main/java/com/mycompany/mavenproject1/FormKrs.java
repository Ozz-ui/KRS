   private void tampilMatkul() {

    DefaultTableModel model =
            new DefaultTableModel() {

        @Override
        public Class<?> getColumnClass(int column) {

            if(column == 0){
                return Boolean.class;
            }

            return Object.class;
        }

        @Override
        public boolean isCellEditable(
                int row,
                int column) {

            return column == 0;
        }
    };

    model.addColumn("Pilih");
    model.addColumn("ID Kelas");
    model.addColumn("Kode MK");
    model.addColumn("Mata Kuliah");
    model.addColumn("SKS");

    try {

        String sql =
        "SELECT kelas.id_kelas, " +
        "mata_kuliah.kode_mk, " +
        "mata_kuliah.nama_mk, " +
        "mata_kuliah.sks " +
        "FROM kelas " +
        "JOIN mata_kuliah " +
        "ON kelas.kode_mk = mata_kuliah.kode_mk";

        PreparedStatement ps =
                conn.prepareStatement(sql);

        ResultSet rs = ps.executeQuery();

        while (rs.next()) {

            model.addRow(new Object[]{
                false,
                rs.getInt("id_kelas"),
                rs.getString("kode_mk"),
                rs.getString("nama_mk"),
                rs.getInt("sks")
            });
        }

        jTable1.setModel(model);

    } catch (Exception e) {

        JOptionPane.showMessageDialog(this,
                e.getMessage());
    }
}
    
    private void loadMahasiswa() {

    try {

        String nim = "2022001002";

        String sql =
        "SELECT m.nama, p.nama_prodi " +
        "FROM mahasiswa m " +
        "JOIN prodi p " +
        "ON m.id_prodi = p.id_prodi " +
        "WHERE m.nim=?";

        PreparedStatement ps =
                conn.prepareStatement(sql);

        ps.setString(1, nim);

        ResultSet rs = ps.executeQuery();

        if(rs.next()) {

            Nim.setText(nim);
            Nama.setText(rs.getString("nama"));
            Prodi.setText(
                    rs.getString("nama_prodi"));

            Semester.setText("3");
            TahunAjaran.setText("2026/2027");
            MaksimalSKS.setText("24");
            StatusKrs.setText("Menunggu");
            TotalSksDipilih.setText("0");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this,
                e.getMessage());
    }
}
    
    private void buatCheckbox() {

    jTable1.getModel()
            .addTableModelListener(e -> {

        int total = 0;

        for (int i = 0;
                i < jTable1.getRowCount();
                i++) {

            Boolean pilih =
            (Boolean) jTable1
            .getValueAt(i, 0);

            if (pilih != null && pilih) {

                total += Integer.parseInt(
                        jTable1.getValueAt(i,4)
                                .toString()
                );
            }
        }

        TotalSksDipilih
                .setText(String.valueOf(total));
    });
}
    
    
    
