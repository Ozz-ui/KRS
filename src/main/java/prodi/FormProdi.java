    private void TambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TambahActionPerformed
        String namaProdi = javax.swing.JOptionPane.showInputDialog(this,
        "Masukkan nama prodi baru:",
        "Tambah Prodi", javax.swing.JOptionPane.PLAIN_MESSAGE);

        if (namaProdi == null || namaProdi.trim().isEmpty()) return;

        database db = new database();
        try (Connection con = db.koneksi();
             PreparedStatement ps = con.prepareStatement(
                 "INSERT INTO prodi (nama_prodi) VALUES (?)")) {

            ps.setString(1, namaProdi.trim());
            ps.executeUpdate();
            javax.swing.JOptionPane.showMessageDialog(this,
                "Prodi berhasil ditambahkan!", "Sukses",
                javax.swing.JOptionPane.INFORMATION_MESSAGE);
            loadData();

        } catch (SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Error: " + e.getMessage(), "Error",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_TambahActionPerformed

    private void HapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HapusActionPerformed
        int baris = jTable1.getSelectedRow();
        if (baris == -1) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Pilih prodi yang ingin dihapus!",
                "Peringatan", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        int konfirmasi = javax.swing.JOptionPane.showConfirmDialog(this,
            "Yakin ingin menghapus prodi ini?",
            "Konfirmasi Hapus", javax.swing.JOptionPane.YES_NO_OPTION);

        if (konfirmasi != javax.swing.JOptionPane.YES_OPTION) return;

        int idProdi = (int) jTable1.getValueAt(baris, 0);

        database db = new database();
        try (Connection con = db.koneksi();
             PreparedStatement ps = con.prepareStatement(
                 "DELETE FROM prodi WHERE id_prodi=?")) {

            ps.setInt(1, idProdi);
            ps.executeUpdate();
            javax.swing.JOptionPane.showMessageDialog(this,
                "Prodi berhasil dihapus!", "Sukses",
                javax.swing.JOptionPane.INFORMATION_MESSAGE);
            loadData();

        } catch (SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Error: " + e.getMessage(), "Error",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_HapusActionPerformed

