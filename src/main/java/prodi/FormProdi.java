    private void EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditActionPerformed
        int baris = jTable1.getSelectedRow();
        if (baris == -1) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Pilih prodi yang ingin diedit!",
                "Peringatan", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        int idProdi = (int) jTable1.getValueAt(baris, 0);
        String namaLama = (String) jTable1.getValueAt(baris, 1);

        String namaBaru = (String) javax.swing.JOptionPane.showInputDialog(this,
            "Edit nama prodi:", "Edit Prodi",
            javax.swing.JOptionPane.PLAIN_MESSAGE, null, null, namaLama);

        if (namaBaru == null || namaBaru.trim().isEmpty()) return;

        database db = new database();
        try (Connection con = db.koneksi();
             PreparedStatement ps = con.prepareStatement(
                 "UPDATE prodi SET nama_prodi=? WHERE id_prodi=?")) {

            ps.setString(1, namaBaru.trim());
            ps.setInt(2, idProdi);
            ps.executeUpdate();
            javax.swing.JOptionPane.showMessageDialog(this,
                "Prodi berhasil diupdate!", "Sukses",
                javax.swing.JOptionPane.INFORMATION_MESSAGE);
            loadData();

        } catch (SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Error: " + e.getMessage(), "Error",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_EditActionPerformed

