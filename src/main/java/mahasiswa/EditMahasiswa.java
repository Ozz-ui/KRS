    private void EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditActionPerformed
        String nimBaru  = fieldNim.getText().trim();
        String nama     = fieldNama.getText().trim();
        String angkatan = fieldAngkatan.getText().trim();

        if (nimBaru.isEmpty() || nama.isEmpty() || angkatan.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Semua field harus diisi!",
                "Peringatan", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        String selectedProdi = jComboBox1.getSelectedItem().toString();
        int idProdi = Integer.parseInt(selectedProdi.split(" - ")[0]);

        database db = new database();
        try (Connection con = db.koneksi();
             PreparedStatement ps = con.prepareStatement(
                 "UPDATE mahasiswa SET nim=?, nama=?, angkatan=?, id_prodi=? WHERE nim=?")) {

            ps.setString(1, nimBaru);
            ps.setString(2, nama);
            ps.setString(3, angkatan);
            ps.setInt(4, idProdi);
            ps.setString(5, nimLama);
            ps.executeUpdate();

            javax.swing.JOptionPane.showMessageDialog(this,
                "Mahasiswa berhasil diupdate!", "Sukses",
                javax.swing.JOptionPane.INFORMATION_MESSAGE);

            new FormMahasiswa().setVisible(true);
            this.dispose();

        } catch (SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Error: " + e.getMessage(), "Error",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_EditActionPerformed

